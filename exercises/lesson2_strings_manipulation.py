# 1. Create a variable first_name and assign it to your first name.
first_name = "Mike"

# 2. Create a variable last_name and assign it to your last name.
last_name = "Golov"

# 3. Create a variable full_name by concatenating first_name and last_name with a space inbetween.
full_name = first_name + " " + last_name

# 4. Print out the full_name
print(f"Full Name: {full_name}")

# 5. Print out the length of full_name using the len() function.
print(f"Length of Full Name: {len(full_name)}")

# 6. Print out the full_name in uppercase letters.
print(f"Uppercase: {full_name.upper()}")

# 7. Print out the full_name in all lowercase letters.
print(f"Lowercase: {full_name.lower()}")